def RPE_layer_coords(mask:np.ndarray|torch.Tensor,use_accelerator:bool=True,return_numpy:bool=False):

    # set device
    if use_accelerator:
        current_device = device
    else:
        current_device = "cpu"
    
    # convert to tensor if necessary
    if isinstance(mask,torch.Tensor):
        if mask.device.type != device.type:
            mask = mask.to(current_device)
    else:
        mask = torch.as_tensor(mask,device=current_device)

    mask = mask == 1
    mask = mask.to(torch.int16)
    mask = torch.where(mask.any(dim=1), mask.shape[1] -1 - torch.argmax(torch.flip(mask,dims=[1]), dim=1), -1)
    mask = mask.cpu()

    coords = valid_coordinates(mask)
    coords = coords.cpu()

    del mask
    gc.collect()
    if use_accelerator:
        torch.cuda.empty_cache()

    if not return_numpy:
        return coords
    else:
        return coords.numpy().astype("uint16")

