def valid_coordinates(z_array:torch.Tensor):
    # make sure this is performed on the cpu
    z_array = z_array.cpu()
    # Find where z is not -1
    mask = z_array != -1

    # Get x and y coordinates
    y_indices, x_indices = torch.where(mask)

    # Get the corresponding z values
    z_values = z_array[y_indices, x_indices]

    # Stack into (N, 3) format: (x, y, z)
    coordinates = torch.column_stack((y_indices,z_values, x_indices))

    return coordinates

