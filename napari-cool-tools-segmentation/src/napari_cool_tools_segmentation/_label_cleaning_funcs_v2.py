@torch.inference_mode()
def generate_circular_mask(data_shape,scale:float=1.0,mask_depth=None,use_input_depth:bool=False,use_accelerator:bool=False,return_numpy:bool=True,verbose:bool=False):
    """"""
    if use_accelerator:
        current_device = device
    else:
        current_device = "cpu"

    height,depth,width = data_shape
    height_center = height // 2
    width_center = width // 2 
    radius = (min(height,width) / 2) * scale

    if use_input_depth:
        mask_depth = depth

    # Create a meshgrid of coordinates
    height_at_coord,width_at_coord = torch.meshgrid(torch.arange(-height_center,height_center,device=current_device),torch.arange(-width_center,width_center,device=current_device),indexing='ij')
    if verbose:
        print(f"height and width coordinate shapes: {height_at_coord.shape,width_at_coord.shape}\n")


    distance_squared = height_at_coord**2 + width_at_coord**2
    # compute circular mask
    circular_mask = distance_squared <= radius**2

    if verbose:
        print("circular mask shape",circular_mask.shape, "mask_depth value", mask_depth)

    if mask_depth:
        circular_mask = circular_mask[:,None,:]
        #circular_mask = torch.repeat_interleave(circular_mask,mask_depth,dim=1)
        circular_mask = circular_mask.expand(-1, mask_depth, -1)


    circular_mask = circular_mask.cpu()
    del distance_squared
    gc.collect()
    torch.cuda.empty_cache()
    if verbose:
        memory_stats()

    if not return_numpy:
        return circular_mask
    else:
        return circular_mask.numpy()

